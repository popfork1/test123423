import { type Server } from "node:http";

import express, {
  type Express,
  type Request,
  Response,
  NextFunction,
} from "express";

import { registerRoutes } from "./routes";
import { db } from "./db";
import { games, news, chatMessages, pickems, pickemRules, standings, playoffMatches, sessions } from "@shared/schema";
import { sql } from "drizzle-orm";

export function log(message: string, source = "express") {
  const formattedTime = new Date().toLocaleTimeString("en-US", {
    hour: "numeric",
    minute: "2-digit",
    second: "2-digit",
    hour12: true,
  });

  console.log(`${formattedTime} [${source}] ${message}`);
}

export const app = express();

declare module 'http' {
  interface IncomingMessage {
    rawBody: unknown
  }
}
app.use(express.json({
  verify: (req, _res, buf) => {
    req.rawBody = buf;
  }
}));
app.use(express.urlencoded({ extended: false }));

app.use((req, res, next) => {
  const start = Date.now();
  const path = req.path;
  let capturedJsonResponse: Record<string, any> | undefined = undefined;

  const originalResJson = res.json;
  res.json = function (bodyJson, ...args) {
    capturedJsonResponse = bodyJson;
    return originalResJson.apply(res, [bodyJson, ...args]);
  };

  res.on("finish", () => {
    const duration = Date.now() - start;
    if (path.startsWith("/api")) {
      let logLine = `${req.method} ${path} ${res.statusCode} in ${duration}ms`;
      if (capturedJsonResponse) {
        logLine += ` :: ${JSON.stringify(capturedJsonResponse)}`;
      }

      if (logLine.length > 80) {
        logLine = logLine.slice(0, 79) + "â€¦";
      }

      log(logLine);
    }
  });

  next();
});

export default async function runApp(
  setup: (app: Express, server: Server) => Promise<void>,
) {
  // Auto-initialize database schema if tables don't exist (for Render compatibility)
  try {
    if (process.env.NODE_ENV === 'production') {
      console.log('Initializing database schema...');
      
      // Create sessions table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS sessions (
          sid VARCHAR PRIMARY KEY,
          sess JSONB NOT NULL,
          expire TIMESTAMP NOT NULL
        )
      `);
      
      // Create users table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS users (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          email VARCHAR UNIQUE,
          first_name VARCHAR,
          last_name VARCHAR,
          profile_image_url VARCHAR,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create games table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS games (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          week INTEGER NOT NULL,
          team1 VARCHAR(100) NOT NULL,
          team2 VARCHAR(100) NOT NULL,
          team1_score INTEGER DEFAULT 0,
          team2_score INTEGER DEFAULT 0,
          quarter VARCHAR(20) DEFAULT 'Scheduled',
          game_time TIMESTAMP,
          location VARCHAR(200),
          is_final BOOLEAN DEFAULT false,
          is_live BOOLEAN DEFAULT false,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create news table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS news (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          title VARCHAR(300) NOT NULL,
          content TEXT NOT NULL,
          excerpt TEXT,
          author_id VARCHAR NOT NULL,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create chat_messages table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS chat_messages (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          username VARCHAR(100) NOT NULL,
          message TEXT NOT NULL,
          game_id VARCHAR,
          created_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create pickems table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS pickems (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          week INTEGER NOT NULL UNIQUE,
          pickem_url TEXT NOT NULL,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create pickem_rules table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS pickem_rules (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          content TEXT NOT NULL,
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create standings table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS standings (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          team VARCHAR(100) NOT NULL,
          division VARCHAR(10) NOT NULL,
          wins INTEGER DEFAULT 0,
          losses INTEGER DEFAULT 0,
          ties INTEGER DEFAULT 0,
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create playoff_matches table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS playoff_matches (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          round VARCHAR(50) NOT NULL,
          match_number INTEGER NOT NULL,
          seed1 INTEGER,
          seed2 INTEGER,
          team1 VARCHAR(100),
          team2 VARCHAR(100),
          team1_score INTEGER,
          team2_score INTEGER,
          winner VARCHAR(100),
          is_complete BOOLEAN DEFAULT false,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create changelogs table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS changelogs (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          version VARCHAR(20) NOT NULL UNIQUE,
          title VARCHAR(200) NOT NULL,
          description TEXT,
          status TEXT NOT NULL,
          changes TEXT NOT NULL,
          date VARCHAR(50) NOT NULL,
          created_at TIMESTAMP DEFAULT NOW(),
          updated_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      // Create predictions table
      await db.execute(sql`
        CREATE TABLE IF NOT EXISTS predictions (
          id VARCHAR PRIMARY KEY DEFAULT gen_random_uuid(),
          game_id VARCHAR NOT NULL,
          voted_for VARCHAR(100) NOT NULL,
          created_at TIMESTAMP DEFAULT NOW()
        )
      `);
      
      console.log('Database schema initialized successfully');
    }
  } catch (error: any) {
    // Tables may already exist - that's fine
    if (!error.message?.includes('already exists')) {
      console.warn('Database initialization warning:', error.message);
    }
  }

  const server = await registerRoutes(app);

  app.use((err: any, _req: Request, res: Response, _next: NextFunction) => {
    const status = err.status || err.statusCode || 500;
    const message = err.message || "Internal Server Error";

    res.status(status).json({ message });
    throw err;
  });

  // importantly run the final setup after setting up all the other routes so
  // the catch-all route doesn't interfere with the other routes
  await setup(app, server);

  // ALWAYS serve the app on the port specified in the environment variable PORT
  // Other ports are firewalled. Default to 5000 if not specified.
  // this serves both the API and the client.
  // It is the only port that is not firewalled.
  const port = parseInt(process.env.PORT || '5000', 10);
  server.listen({
    port,
    host: "0.0.0.0",
    reusePort: true,
  }, () => {
    log(`serving on port ${port}`);
  });
}
